-- =====================================================================
-- SECTION 1: Create app_role enum type
-- =====================================================================
-- Defines the three valid role values at the database level
-- Ensures type safety and prevents invalid role assignments

DO $$ BEGIN
  CREATE TYPE public.app_role AS ENUM ('user', 'moderator', 'admin');
EXCEPTION
  WHEN duplicate_object THEN NULL;
END $$;

-- =====================================================================
-- SECTION 2: Create user_roles table
-- =====================================================================
-- Stores role assignments for users
-- One role per user (enforced by unique constraint on user_id)

CREATE TABLE IF NOT EXISTS public.user_roles (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL UNIQUE,
  role public.app_role DEFAULT 'user'::public.app_role NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- =====================================================================
-- SECTION 3: Create indexes for performance
-- =====================================================================
-- Optimizes role lookups and filtering operations

CREATE INDEX IF NOT EXISTS idx_user_roles_user_id ON public.user_roles(user_id);
CREATE INDEX IF NOT EXISTS idx_user_roles_role ON public.user_roles(role);

-- =====================================================================
-- SECTION 4: Revoke all public permissions
-- =====================================================================
-- Implements "Do Not Trust The Client" paradigm
-- Only supabase_auth_admin can directly access this table

REVOKE ALL ON public.user_roles FROM public;
REVOKE ALL ON public.user_roles FROM anon;
REVOKE ALL ON public.user_roles FROM authenticated;

-- Grant full access to supabase_auth_admin (used by JWT hook)
GRANT ALL ON public.user_roles TO supabase_auth_admin;


-- =====================================================================
-- SECTION 5: Create custom_access_token_hook function
-- =====================================================================
-- This function is triggered by Supabase Auth during authentication
-- It stamps the user's role directly into the JWT claims
-- Zero database lookups required for subsequent role checks

CREATE OR REPLACE FUNCTION public.custom_access_token_hook(event jsonb)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  claims jsonb;
  user_role public.app_role;
BEGIN
  -- Extract claims from authentication event
  claims := event->'claims';
  
  -- Fetch user role from secure table
  -- This is the ONLY database lookup for role checking
  SELECT role INTO user_role 
  FROM public.user_roles 
  WHERE user_id = (event->>'user_id')::uuid;
  
  -- Inject role into JWT claims under 'user_role' key
  IF user_role IS NOT NULL THEN
    claims := jsonb_set(claims, '{user_role}', to_jsonb(user_role::text));
  ELSE
    -- Default to 'user' if no role found (safety fallback)
    claims := jsonb_set(claims, '{user_role}', '"user"');
  END IF;
  
  -- Return modified event with updated claims
  event := jsonb_set(event, '{claims}', claims);
  
  RETURN event;
EXCEPTION
  WHEN OTHERS THEN
    -- Critical: On error, default to 'user' role to prevent auth failures
    -- Log error for debugging but don't block authentication
    RAISE WARNING 'Error in custom_access_token_hook for user %: %', 
      event->>'user_id', SQLERRM;
    claims := jsonb_set(claims, '{user_role}', '"user"');
    event := jsonb_set(event, '{claims}', claims);
    RETURN event;
END;
$$;

-- Lock down function execution
REVOKE ALL ON FUNCTION public.custom_access_token_hook(jsonb) FROM public;
REVOKE ALL ON FUNCTION public.custom_access_token_hook(jsonb) FROM anon;
REVOKE ALL ON FUNCTION public.custom_access_token_hook(jsonb) FROM authenticated;

-- Grant execute permission only to supabase_auth_admin
GRANT EXECUTE ON FUNCTION public.custom_access_token_hook(jsonb) TO supabase_auth_admin;

-- Add function comment for documentation
COMMENT ON FUNCTION public.custom_access_token_hook(jsonb) IS 
'Custom JWT access token hook that stamps user roles into authentication tokens. Must be enabled in Supabase Dashboard: Authentication → Hooks → Custom Access Token.';


-- =====================================================================
-- SECTION 6: Create role_audit_log table
-- =====================================================================
-- Maintains complete audit trail of all role changes
-- Critical for security compliance and troubleshooting

CREATE TABLE IF NOT EXISTS public.role_audit_log (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  old_role public.app_role,
  new_role public.app_role NOT NULL,
  changed_by UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  changed_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Create indexes for efficient audit log queries
CREATE INDEX IF NOT EXISTS idx_role_audit_user_id ON public.role_audit_log(user_id);
CREATE INDEX IF NOT EXISTS idx_role_audit_changed_at ON public.role_audit_log(changed_at DESC);

-- =====================================================================
-- SECTION 7: Create log_role_changes trigger function
-- =====================================================================
-- Automatically logs all role changes to audit table
-- Captures INSERT and UPDATE operations with actor information

CREATE OR REPLACE FUNCTION public.log_role_changes()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  -- Log UPDATE operations when role actually changes
  IF TG_OP = 'UPDATE' AND OLD.role IS DISTINCT FROM NEW.role THEN
    INSERT INTO public.role_audit_log (user_id, old_role, new_role, changed_by)
    VALUES (NEW.user_id, OLD.role, NEW.role, auth.uid());
  
  -- Log INSERT operations (new role assignments)
  ELSIF TG_OP = 'INSERT' THEN
    INSERT INTO public.role_audit_log (user_id, old_role, new_role, changed_by)
    VALUES (NEW.user_id, NULL, NEW.role, auth.uid());
  END IF;
  
  RETURN NEW;
END;
$$;

-- Attach trigger to user_roles table
DROP TRIGGER IF EXISTS on_role_change ON public.user_roles;
CREATE TRIGGER on_role_change
  AFTER INSERT OR UPDATE ON public.user_roles
  FOR EACH ROW
  EXECUTE FUNCTION public.log_role_changes();

COMMENT ON FUNCTION public.log_role_changes() IS 
'Trigger function that automatically logs all role changes to role_audit_log table for security compliance.';


-- =====================================================================
-- SECTION 8: Create auto-role assignment trigger for new users
-- =====================================================================
-- Automatically assigns 'user' role to new registrations
-- Ensures all authenticated users have a valid role in the system

CREATE OR REPLACE FUNCTION public.handle_new_user_role()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  -- Insert default 'user' role for new user
  INSERT INTO public.user_roles (user_id, role)
  VALUES (NEW.id, 'user');
  
  RETURN NEW;
EXCEPTION
  WHEN OTHERS THEN
    -- Critical: Log error but don't fail user creation
    -- User will get default 'user' role from JWT hook fallback
    RAISE WARNING 'Failed to create default role for user %: %', NEW.id, SQLERRM;
    RETURN NEW;
END;
$$;

-- Attach trigger to auth.users table
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW
  EXECUTE FUNCTION public.handle_new_user_role();

COMMENT ON FUNCTION public.handle_new_user_role() IS 
'Trigger function that automatically assigns default "user" role to new user registrations.';


