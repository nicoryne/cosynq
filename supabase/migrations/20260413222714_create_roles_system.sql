ALTER TABLE public.user_roles ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.role_audit_log ENABLE ROW LEVEL SECURITY;

-- =====================================================================
-- SECTION 10: RLS Policies for user_roles table
-- =====================================================================

-- Policy: Admins can manage all user roles
-- Extracts 'user_role' claim from JWT and checks for 'admin' value
DROP POLICY IF EXISTS "Admins can manage user roles" ON public.user_roles;
CREATE POLICY "Admins can manage user roles"
ON public.user_roles
FOR ALL
USING ((auth.jwt() ->> 'user_role')::text = 'admin')
WITH CHECK ((auth.jwt() ->> 'user_role')::text = 'admin');

-- Policy: Users can view their own role
-- Allows users to see their own role assignment
DROP POLICY IF EXISTS "Users can view their own role" ON public.user_roles;
CREATE POLICY "Users can view their own role"
ON public.user_roles
FOR SELECT
USING (auth.uid() = user_id);

-- =====================================================================
-- SECTION 11: RLS Policies for role_audit_log table
-- =====================================================================

-- Policy: Admins can view all audit logs
DROP POLICY IF EXISTS "Admins can view audit logs" ON public.role_audit_log;
CREATE POLICY "Admins can view audit logs"
ON public.role_audit_log
FOR SELECT
USING ((auth.jwt() ->> 'user_role')::text = 'admin');

-- Policy: Users can view their own audit log
DROP POLICY IF EXISTS "Users can view their own audit log" ON public.role_audit_log;
CREATE POLICY "Users can view their own audit log"
ON public.role_audit_log
FOR SELECT
USING (auth.uid() = user_id);

